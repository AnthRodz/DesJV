 void ListarPrestamo() {
        List <Prestamos> Listar = prestamosBD.Listar();
        modelo = (DefaultTableModel) TablaPrestamos.getModel();
        Object[] ob = new Object[6];
     
        for (int i = 0; i < Listar.size(); i++) {
            ob[0] = Listar.get(i).getIdPrestamo();
            ob[1] = Listar.get(i).getIdLibro();
            ob[2] = Listar.get(i).getIdUsuario();
            ob[3] = Listar.get(i).getFechaPrestamo();
            ob[4] = Listar.get(i).getFechaDevolucion();
            ob[5] = Listar.get(i).getDevuelto();
            modelo.addRow(ob);
        }
        TablaPrestamos.setModel(modelo);
        //color( TablaPrestamos);
    }
  public void LimpiarTable() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }
