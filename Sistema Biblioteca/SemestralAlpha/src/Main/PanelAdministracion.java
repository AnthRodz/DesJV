    private void TablaPrestamosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaPrestamosMouseClicked
       int row = TablaPrestamos.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "No se Selecciono");
        } else {
           int  idpres = Integer.parseInt((String) TablaPrestamos.getValueAt(row, 0).toString());
            int idlibro = Integer.parseInt((String)  TablaPrestamos.getValueAt(row, 1).toString());
              int idestu = Integer.parseInt((String)  TablaPrestamos.getValueAt(row, 2).toString());
            String fechapres = (String)  TablaPrestamos.getValueAt(row, 3);
            String fechadev =  (String)  TablaPrestamos.getValueAt(row, 4);
           
            campoIDPrestamo.setText(""+idpres);
            campoIDLibro.setText("" + idlibro);
            campoIDEstudiante.setText(""+idestu);
            campoPrestamo.setText(""+fechapres);
            campoDevolucion.setText(""+fechadev);

        }
        
        
        
        
    }//GEN-LAST:event_TablaPrestamosMouseClicked

